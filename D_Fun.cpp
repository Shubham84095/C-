void Solution()
{
    int n, x;
    cin >> n >> x;

    ll ans = 0;
    for(int a = 1; a < n; a++){
        for(int b = 1; b*a < n && b+a < x; b++){
            ans += min(x-a-b, (n-a*b)/(a+b));
        }
    }
    cout << ans << endl;
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while(t--) 
    {
        Solution();
    }

    return 0;
}