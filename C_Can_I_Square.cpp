void Solution()
{
    int n;
    cin >> n;
    double sum = 0, temp;

    while(n--){
        cin >> temp;
        sum += temp;
    }

    if(sqrt(sum) - (long long int)(sqrt(sum)) < 0.000000001){
        cout << "YES\n";
    }
    else {
        cout << "NO\n";
    }
}

