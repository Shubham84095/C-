void Solution()
{
    int x, y, k;
    cin >> x >> y >> k;

    int a = ceil((1.0*x)/k), b = ceil((1.0*y)/k);

    if(a > b){
        cout << max(2*a-1, 0LL) << endl;
    }else{
        cout << 2*b << endl;
    }
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while(t--) 
    {
        Solution();
    }

    return 0;
}